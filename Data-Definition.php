  <h3>Definition of specific terms used on the website </h3>
  <p>
    Country: a nation with its own government, occupying a particular territory. In this website , we will use the country which are Member states of the United Nations.
  </p>
  <p>
    Continents: A continent is one of several very large landmasses. Generally identified by convention rather than any strict criteria. 
    We will use the following regions on this website:  Asia, Africa, North America, South America, Europe, and Oceania.
  </p>
    <p>
    GDP meaning: Gross Domestic Product is the total value of goods and services produced in a country. It is an economic indicator used worldwide to show the economic health of a country.
  </p>
    <p>
    HDI meaning: Human Development Index , the calculation of the index combines four major indicators: life expectancy for health, expected years of schooling, 
    mean of years of schooling for education and Gross National Income per capita for standard of living.
  </p>
    <p>
    Population: is defined as all nationals present in, or temporarily absent from a country, and aliens permanently settled in a country. 
  </p>
    <p>
    Size: measure the physical territory of a Country (land mass).
  </p>




